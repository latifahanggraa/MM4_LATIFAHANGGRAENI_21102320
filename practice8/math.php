<?php
    //PHP pi() Function
    echo(pi());
    echo "<br>";

    //PHP min() and max() Functions
    echo(min(0, 150, 30, 20, -8, -200));
    echo(max(0, 150, 30, 20, -8, -200));
    echo "<br>";

    //PHP abs() Function
    echo(abs(-6.7));
    echo "<br>";

    //PHP sqrt() Function
    echo(sqrt(64));
    echo "<br>";

    //PHP round() Function
    echo(round(0.60));
    echo(round(0.49));
    echo "<br>";

    //Random Numbers
    echo(rand());

    echo(rand(10, 100));
    echo "<br>";
?>